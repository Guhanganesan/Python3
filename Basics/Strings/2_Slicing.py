print(x)        #QWERTY
print(x[0])     #Q
print(x[-1])    #Y
print(x[1])     #W
print(x[1:])    #includes 1
print(x[:4])    #omits 4
print(x[::-1])  #REVERSE THE STRING
print(x[1:5])   #WERT -> before 5
print(x[-3:-1]) #RT  -> before -1
print(x[3:]+x[:3]) #RTY  + QWE

